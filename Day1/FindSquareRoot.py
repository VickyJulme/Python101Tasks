num1 = int(input(" find the square root of number   :"))

result = num1**0.5

print(result)



# The ** operator in Python is used for exponentiation, which means raising a number to the power of another number. In this context, when you use ** 0.5, you are raising the number to the power of 0.5, which is mathematically equivalent to finding the square root.

# Here's a bit more detail:

# num ** 2 means num raised to the power of 2 (squaring the number).

# num ** 3 means num raised to the power of 3 (cubing the number).

# num ** 0.5 means num raised to the power of 0.5 (finding the square root of the number).